def adj_to_bias(adj, sizes, nhood=1):
    """
     Prepare adjacency matrix by expanding up to a given neighbourhood.
     This will insert loops on every node.
     Finally, the matrix is converted to bias vectors.
     Expected shape: [graph, nodes, nodes]
    """
    nb_graphs = adj.shape[0]
    mt = np.empty(adj.shape)
    for g in range(nb_graphs):
        mt[g] = np.eye(adj.shape[1])
        for _ in range(nhood):
            mt[g] = np.matmul(mt[g], (adj[g] + np.eye(adj.shape[1])))
        for i in range(sizes[g]):
            for j in range(sizes[g]):
                if mt[g][i][j] > 0.0:
                    mt[g][i][j] = 1.0
    return -1e9 * (1.0 - mt)


def salt_and_pepper(input, noise=0.2):
    a = np.random.binomial(n=1, p=(1-noise), size=input.shape)
    b = np.random.binomial(n=1, p=0.5, size=input.shape)
    c = np.equal(a, 0) * b
    return input * a + c


def largest_eigval_smoothing_filter(adj):
    adj_sparse = sp.coo_matrix(adj)
    ident = sp.eye(adj_sparse.shape[0])

    adj_normalized = adj_sparse + ident

    rowsum = np.array(adj_normalized.sum(1))
    d_inv_sqrt = np.power(rowsum, -0.5).flatten()
    d_inv_sqrt[np.isinf(d_inv_sqrt)] = 0.
    d_inv_sqrt_sparse = sp.diags(d_inv_sqrt)

    laplacian = sp.csgraph.laplacian(adj_normalized, normed=True) # the symmetric normalized laplacian

    largest_eigval, _ = eigsh(laplacian, 1, which='LM')
    largest_eigval = largest_eigval.round(decimals=4) # round to avoid floating point overflow

    h = ident - laplacian.multiply(1 / largest_eigval)
    return h


